package Model;

/**
 *
 * @author fabri
 */
public class Airplane {

    private int id;
    private String name;
    private int places;

    public Airplane () {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPlaces() {
        return places;
    }

    public void setPlaces(int places) {
        this.places = places;
    }

}

package Model;

/**
 *
 * @author marti
 */
public class Reservation {

    private int id;
    private User user;
    private Flight flight;

    public Reservation(int id, User user, Flight flight) {
        this.id = id;
        this.user = user;
        this.flight = flight;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Flight getFlight() {
        return flight;
    }

    public void setFlight(Flight flight) {
        this.flight = flight;
    }

}
